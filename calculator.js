function calculateResult(answers, selectedQuestions) {
  var scores = { dominance: 0, emotion: 0, mask: 0, fate: 0, drama: 0 };

  answers.forEach(function(answer) {
    if (answer.scores) {
      Object.keys(answer.scores).forEach(function(key) {
        scores[key] += answer.scores[key];
      });
    }
  });

  var normalized = {
    dominance: scores.dominance >= 0 ? 1 : -1,
    emotion: scores.emotion >= 0 ? 1 : -1,
    mask: scores.mask >= 0 ? 1 : -1,
    fate: scores.fate >= 0 ? 1 : -1,
    drama: scores.drama >= 0 ? 1 : -1
  };

  var bestMatch = null;
  var bestScore = -Infinity;

  ROLE_MAP.forEach(function(role) {
    var matchScore = 0;
    Object.keys(role.pattern).forEach(function(key) {
      if (role.pattern[key] === normalized[key]) {
        matchScore += 2;
      }
    });
    Object.keys(role.pattern).forEach(function(key) {
      var diff = Math.abs(scores[key] - role.pattern[key] * Math.max(Math.abs(scores[key]), 1));
      matchScore -= diff * 0.05;
    });

    if (matchScore > bestScore) {
      bestScore = matchScore;
      bestMatch = role.id;
    }
  });

  var maxDim = 30;
  var percentages = {};
  Object.keys(scores).forEach(function(key) {
    percentages[key] = Math.min(100, Math.max(0, Math.round(((scores[key] + maxDim) / (maxDim * 2)) * 100)));
  });

  return {
    roleId: bestMatch,
    role: ROLES[bestMatch],
    scores: scores,
    percentages: percentages
  };
}
